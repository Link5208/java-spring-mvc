    public Page<Product> fetchProducts(Pageable page, ProductCriteriaDTO productCriteriaDTO) {
        if (productCriteriaDTO.getTarget() == null &&
                productCriteriaDTO.getFactory() == null &&
                productCriteriaDTO.getPrice() == null) {
            return this.productRepository.findAll(page);

        }
        Specification<Product> combinedSpec = Specification.where(null);

        if (productCriteriaDTO.getTarget() != null &&
                productCriteriaDTO.getTarget().isPresent()) {
            Specification currentSpecs = ProductSpecs.matchListTarget(productCriteriaDTO.getTarget().get());
            combinedSpec = combinedSpec.and(currentSpecs);
        }
        if (productCriteriaDTO.getFactory() != null &&
                productCriteriaDTO.getFactory().isPresent()) {
            Specification currentSpecs = ProductSpecs.matchListFactory(productCriteriaDTO.getFactory().get());
            combinedSpec = combinedSpec.and(currentSpecs);

        }
        if (productCriteriaDTO.getPrice() != null &&
                productCriteriaDTO.getPrice().isPresent()) {
            Specification currentSpecs = this.buildPriceSpecification(productCriteriaDTO.getPrice().get());
            combinedSpec = combinedSpec.and(currentSpecs);

        }
        return this.productRepository.findAll(combinedSpec, page);
    }

